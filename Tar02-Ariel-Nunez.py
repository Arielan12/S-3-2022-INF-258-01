Numero2=int(input("De que numero desea saber la raiz:"))
#Ya aqui despues de haber puesto las variables proseguimos a imprimir los resultados de las variables
#Aqui elevamos nuestras variables al cuadrado y procedemos con el ejercicio.
print("elevando al cuadrado:",Numero1,"=",Numero1**2)
print("elevando al cuadrado:",Numero2,"=",Numero2**2)
